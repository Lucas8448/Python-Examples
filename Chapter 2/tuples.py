
# Tuples in Python

# Creating a tuple
person = ("John Doe", 30, 5.9)
print("Person: ", person)

# Accessing tuple elements
print("Name: ", person[0])

# Tuples are immutable, so we cannot modify elements
# person[0] = "Jane Doe"  # This would raise an error

# Length of a tuple
print("Length of tuple: ", len(person))
